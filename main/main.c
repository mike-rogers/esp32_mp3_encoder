static void conv_task(void *arg)
{
    vTaskDelay(pdMS_TO_TICKS(1000));
#ifdef ENABLE_SPIFFS
    spiffs_open();
    char* argv[] = { "x", "/spiffs/in.wav", "/spiffs/out.mp3" };
    convert(3, argv);
    spiffs_close();
#else
    sdcard_open();
    char* argv[] = { "x", "/sdcard/in.wav", "/sdcard/out.mp3" };
    convert(3, argv);
    sdcard_close();
#endif
    vTaskDelete(NULL);
}

